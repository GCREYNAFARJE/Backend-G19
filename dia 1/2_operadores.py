numero1 = 10
numero2 = 20

#Aritmeticos
print(numero1 + numero2) #suma
print(numero1 - numero2) #resta
print(numero1 * numero2) #multiplicacion

# Comparacion (Boolean)
print(numero1 == numero2) # igual que 
print(numero1 != numero2) # diferente que  
print(numero1 > numero2) # mayor que  
print(numero1 < numero2) # menor que  

# Logicos 
print(numero1 > numero2 and numero1 !=numero2)
print(numero1 > numero2 or numero1 !=numero2)
print(not numero1 > numero2) # invierte el resultado de la comparacion

